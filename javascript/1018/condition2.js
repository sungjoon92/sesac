// boolean
if (true) {
  console.log(true);
}

if (false) {
  console.log(true);
} else {
  console.log(false);
}

// 비교연산자

if (1 < 2) {
  console.log('1 < 2');
}

// 논리 연산자
if (true && true) {
  console.log('true && true');
}

// 숫자도 가능
if (1) {
  console.log(1);
}

if (-1) {
  console.log(-1);
}

if (0) {
  console.log('실행 안됨');
}

if ('abc') {
  console.log('abc');
}

if ('') {
  console.log('빈 문자열');
}

if (' ') {
  console.log('공백');
}

if (null) {
  console.log('null');
}
